<h1 style="font-size:220%;"> Placement Details </h1>
<p>
Faltu College of Engineering has Corporate Relations & Placement Cell dedicated for strengthening the interaction between Institute and Industry. The cell is coordinated by a senior faculty, who will be supported by one faculty member from each of the departments. The students also actively participate in the activities as volunteers.
The basic purpose of this cell is to involve industry’s inputs in the form of expert lectures, short-term training for faculty members/ students, projects, laboratory development, sponsoring technical non technical events, placements, etc.
</p>

<h2> Facilities made available to the students</h2>

<p>1. Arranging Campus Interviews of various organizations for final year students for their recruitments</p>
<p>2. Arranging guest lectures of Eminent Personalities from Industry for career counseling</p>
<p>3. Co-ordination for Industrial visits arranged by various departments.</p>
<p>4. Co-ordinations with the various departments through T & P for arranging Technical Seminars, Arranging Mock Interviews, Written Test & Group Discussion for the final year students.</p>
