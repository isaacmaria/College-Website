<h1 style="font-size:220%;"> Placement Policy</h2>

<h3>Purpose</h3>
<p>This policy lays down the procedures for assisting the students to find placement in the industry at the end of the program. The policy also highlights the Standard operating procedures  to be followed by Final Year students of UG and PG Courses.</p>
<h3>Scope</h3>
<p>Procedures laid down in this document are applicable to all the eligible and interested students of final year (UG & PG courses).</p>
</article>

<article>
<h1 style="font-size:220%;">Placement Procedure and Guidelines</h1>
<p>


1.    Invitation to be sent to the companies for Campus Recruitment Program. <br>
2.    Invitation to be sent to distinguished alumni of Institute working at senior positions for Campus Recruitment Program.<br>
3.    If an organization is offered a Dream Status by college (where the Company CTC is Rs.5.00 LPA and above ) all eligible students are allowed to register.<br>
4.    Once registered, student will be required to attend the pre-placement talk.<br>
5.    Once the student opts to appear for the Aptitude Test, the entire cycle has to be completed, i.e. if short listed for Group Discussion and/ or Personal Interview; the candidate MUST attend.<br>
6.    After Registration for Recruitment Process, if any student is absent for Recruitment process, he must submit an application to Training & Placement Officer with genuine reason.<br>
7.    Students should not directly interact with HR Executives of Company before Pre-placement Talk.<br>
8.    Students should keep, Xerox copies of Mark sheets and certificates during the recruitment process     <br>  
9.    It is advised to keep ready 2 Passport size photographs and a hard copy of resume. <br>
10.    Students should keep Xerox copies of Pan Card and Aadhar Card or Passport during the recruitment process. <br>
11.    Wearing formal attire during the recruitment process is Mandatory <br>
12.    No Candidate/Student will be entertained in Campus Recruitment Program (CRP) after the deadline of registration. <br>
13.    Students with Poor attendance/academic performance and cases of indiscipline/malpractices will not be eligible for participation in the placement process. <br>
14.    Student coordinators should be mix of 4(1 TE + 2 BE + 1ME) students. <br>
15.    Information about the schedule of recruitment process will be communicated to the students on e-mail ID, Notice board, Facebook whichever is feasible. <br>
16.    Announcements regarding recruitment process will be posted to Facebook, email ID and Notice Board from time to time. <br>
17.    Interested students are required to register via the link displayed on Facebook and email ID. <br>
18.    Usually a Pre Placement Talk (PPT) is conducted by the organization explaining the students; job profile, service agreement etc. The students are requested to get any doubts clarified during this talk.(including the doubts regarding CTC,Fixed pay, variable pay and other details regarding CTC) <br>
19.    A database of the students will be created as per company database format. <br>
20.    Students should mention in CV/Bio-data Interested area of working. <br>
21.    Mail the LATEST copy of your resume whenever asked for. <br>
22.    During the Recruitment process, student should submit CV/Bio-data in the format given by Institute with template of KJ Somaiya College of Engineering.(Flexibility will be allowed as per requirement to edit CV) <br>
23.    Students are required to remain present at least 15 minutes before the scheduled time of PPT or aptitude test. Late comers will not be allowed to appear for the process.  <br>
24.    Students are allowed to take maximum two job offers (including dream).<br>
25.    If selected in a dream company, students are not allowed to appear for placement for any other dream company. <br>
26.    If a student is selected in two companies, he/she will be asked to join the second company but with option of whichever company gives offer letter first. <br>
27.    Selected student must submit Xerox copy of offer letter to Training & Placement Officer and the Department Faculty coordinator<br>
</p>
